package com.greenfoxacademy.bankofsimba.Models;

public class BankAccount {
  private String name;
  private double balance;
  private String animalType;
  
  
  public BankAccount() {
  }
  
  public String getName() {
    return name;
  }
  
  public double getBalance() {
    return balance;
  }
  
  public String getAnimalType() {
    return animalType;
  }
}
